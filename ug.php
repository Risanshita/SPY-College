In India the Graduation system is classified into two parts: Undergraduation (UG) and Postgraduation (PG). It takes three or four years to complete an "undergraduate" degree. The three-year undergraduate courses  are mostly in the fields of arts, commerce, science etc., and the four-year programs are mostly in the fields of technology, engineering, pharmaceutical sciences, agriculture etc. However, for medicine, law and architecture, the period has been five years.[3] The possessor of the first UG is referred to as graduate and that of the PG degree as post-graduate.<br /> 
As per courses of studies regulated by Magadh University, third year degree courses  at undergraduate level there is of two levels.One is at Honours level while other is at pass course level.<br />
The students who  obtain at least 45% marks in any subject in intermediate may study in Honours level courses while who do not obtain 
at least 45% marks in any subject in intermediate may study in pass course level.The students of Honours level corses in any decipline may choose one subject as Honours(of two papers),two subjects as subsidiary and any one subject of language as composition in dgree part-1 and such repeats in degree part-2.In degree part-3(Final) Honours subject  repeats (of four papers) and one paper as General studies.Thus total honours papers are of 800 marks and grand total is of 1500 marks.<br />
The students of pass course level choose three papers as optional  and any one subject of language as composition in dgree part-1 and such repeats in degree part-2. In degree part-3(Final) all optional subjects  reapeat and one paper as general studies .
Thus total  papers are of 1200 marks.


